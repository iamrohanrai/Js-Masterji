const month = 6;
switch (month) {
  case 1:
    console.log("january");
    break;
  case 2:
    console.log("feb");
    break;
  case 3:
    console.log("march");
    break;
  case 4:
    console.log("april");
    break;
  case 5:
    console.log("may");
    break;

  default:
    console.log("default case");
    break;
}

// if we dont write break then code match ke baad sara case log hoga except default
